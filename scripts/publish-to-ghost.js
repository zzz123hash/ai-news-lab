import crypto from 'node:crypto';

const {
	GHOST_ADMIN_API_URL,
	GHOST_ADMIN_API_KEY,
	GHOST_API_VERSION = 'v6.0',
	GHOST_POST_TITLE = 'AI Generated Draft: Building a Practical AI News Workflow',
	GHOST_POST_EXCERPT = 'A draft article generated by the AI content workflow for editorial review.',
	GHOST_POST_TAGS = 'AI, Workflow, Draft',
	GHOST_POST_AUTHOR_EMAIL,
	GHOST_POST_VISIBILITY = 'public',
	GHOST_POST_META_TITLE,
	GHOST_POST_META_DESCRIPTION,
	GHOST_POST_MARKDOWN,
} = process.env;

const DEFAULT_MARKDOWN = `## Summary

AI news can become more useful when it is turned into a repeatable editorial workflow instead of a stream of disconnected links.

## Draft angle

Use this draft as a starting point for a short practical article. Verify every factual claim before publishing.

## Workflow

- Collect source links and notes.
- Generate a structured outline.
- Review the draft for accuracy and tone.
- Publish as a Ghost draft first, then edit in Ghost Admin.
`;

async function main() {
	if (!GHOST_ADMIN_API_URL) {
		throw new Error('GHOST_ADMIN_API_URL is required');
	}

	if (!GHOST_ADMIN_API_KEY) {
		throw new Error('GHOST_ADMIN_API_KEY is required');
	}

	const adminApiUrl = normalizeAdminApiUrl(GHOST_ADMIN_API_URL);
	const endpoint = new URL('posts/?source=html', adminApiUrl);
	const token = createGhostAdminToken(GHOST_ADMIN_API_KEY);
	const title = GHOST_POST_TITLE.trim();
	const post = {
		title,
		html: markdownToHtml(GHOST_POST_MARKDOWN || DEFAULT_MARKDOWN),
		status: 'draft',
		visibility: GHOST_POST_VISIBILITY,
		custom_excerpt: GHOST_POST_EXCERPT,
		tags: parseCsv(GHOST_POST_TAGS),
		meta_title: GHOST_POST_META_TITLE || title,
		meta_description: GHOST_POST_META_DESCRIPTION || GHOST_POST_EXCERPT,
	};

	if (GHOST_POST_AUTHOR_EMAIL) {
		post.authors = [GHOST_POST_AUTHOR_EMAIL.trim()];
	}

	const response = await fetch(endpoint, {
		method: 'POST',
		headers: {
			Authorization: `Ghost ${token}`,
			'Accept-Version': GHOST_API_VERSION,
			'Content-Type': 'application/json',
		},
		body: JSON.stringify({ posts: [post] }),
	});
	const responseText = await response.text();
	const responseBody = parseJson(responseText);

	if (!response.ok) {
		const detail = responseBody?.errors?.[0]?.message || responseBody?.message || responseText || `HTTP ${response.status}`;
		throw new Error(`Ghost draft creation failed: ${detail}`);
	}

	const createdPost = responseBody?.posts?.[0];

	console.log(
		JSON.stringify(
			{
				ok: true,
				id: createdPost?.id,
				title: createdPost?.title,
				slug: createdPost?.slug,
				status: createdPost?.status,
				visibility: createdPost?.visibility,
				url: createdPost?.url,
			},
			null,
			2,
		),
	);
}

function normalizeAdminApiUrl(rawUrl) {
	const url = new URL(rawUrl);
	let pathname = url.pathname.replace(/\/+$/, '');

	if (!pathname.endsWith('/ghost/api/admin')) {
		pathname = `${pathname}/ghost/api/admin`.replace(/\/{2,}/g, '/');
	}

	url.pathname = `${pathname}/`;
	url.search = '';
	url.hash = '';

	return url;
}

function createGhostAdminToken(adminApiKey) {
	const [id, secret] = adminApiKey.split(':');

	if (!id || !secret || !/^[a-f0-9]+$/i.test(secret)) {
		throw new Error('GHOST_ADMIN_API_KEY must look like <id>:<hex-secret>');
	}

	const now = Math.floor(Date.now() / 1000);
	const header = {
		alg: 'HS256',
		typ: 'JWT',
		kid: id,
	};
	const payload = {
		iat: now,
		exp: now + 5 * 60,
		aud: '/admin/',
	};
	const encodedHeader = base64UrlEncode(JSON.stringify(header));
	const encodedPayload = base64UrlEncode(JSON.stringify(payload));
	const unsignedToken = `${encodedHeader}.${encodedPayload}`;
	const signature = crypto
		.createHmac('sha256', Buffer.from(secret, 'hex'))
		.update(unsignedToken)
		.digest('base64url');

	return `${unsignedToken}.${signature}`;
}

function markdownToHtml(markdown) {
	const lines = markdown.replace(/\r\n/g, '\n').trim().split('\n');
	const blocks = [];
	let paragraph = [];
	let list = [];

	const flushParagraph = () => {
		if (paragraph.length > 0) {
			blocks.push(`<p>${paragraph.map(escapeHtml).join(' ')}</p>`);
			paragraph = [];
		}
	};
	const flushList = () => {
		if (list.length > 0) {
			blocks.push(`<ul>${list.map((item) => `<li>${escapeHtml(item)}</li>`).join('')}</ul>`);
			list = [];
		}
	};

	for (const line of lines) {
		const trimmed = line.trim();

		if (!trimmed) {
			flushParagraph();
			flushList();
			continue;
		}

		if (trimmed.startsWith('## ')) {
			flushParagraph();
			flushList();
			blocks.push(`<h2>${escapeHtml(trimmed.slice(3))}</h2>`);
			continue;
		}

		if (trimmed.startsWith('# ')) {
			flushParagraph();
			flushList();
			blocks.push(`<h1>${escapeHtml(trimmed.slice(2))}</h1>`);
			continue;
		}

		if (trimmed.startsWith('- ')) {
			flushParagraph();
			list.push(trimmed.slice(2));
			continue;
		}

		flushList();
		paragraph.push(trimmed);
	}

	flushParagraph();
	flushList();

	return blocks.join('\n');
}

function parseCsv(value) {
	return value
		.split(',')
		.map((item) => item.trim())
		.filter(Boolean);
}

function parseJson(value) {
	try {
		return value ? JSON.parse(value) : null;
	} catch {
		return null;
	}
}

function base64UrlEncode(value) {
	return Buffer.from(value).toString('base64url');
}

function escapeHtml(value) {
	return value
		.replace(/&/g, '&amp;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;')
		.replace(/"/g, '&quot;')
		.replace(/'/g, '&#39;');
}

main().catch((error) => {
	console.error(error instanceof Error ? error.message : error);
	process.exitCode = 1;
});
